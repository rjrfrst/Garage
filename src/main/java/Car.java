public class Car {
    //Properties
    private int wheels;
    private int doors;

    //Constructors
    public Car(int inputWheels, int inputDoors){
        this.wheels = inputWheels;
        this.doors = inputDoors;
    }

    //Methods
    //add an extra wheel for the car
    public int addWheel(){
        return this.addWheel();
    }

    //Getters and Setters


    public int getWheels() {
        return wheels;
    }
    public void setWheels(int wheels) {
        this.wheels = wheels;
    }

    public int getDoors(){
        return doors;
    }
    public void setDoors(int doors){
        this.doors = doors;
    }

} // Last
