public class Motorbike {

    //Properties
    private int wheel;
    private int helmetCase;

    //Constructors
    public Motorbike(int inputWheel, int inputHelmetCase){
        this.wheel = inputWheel;
        this.helmetCase = inputHelmetCase;
    }

    //Methods
    //add a helmet case for the bike
    public int addHelmetCase(){
        return this.addHelmetCase();
    }

    //Getters and Setters
    public int getWheel() {
        return wheel;
    }
    public void setWheel(int wheel) {
        this.wheel = wheel;
    }

    public int getHelmetCase(){
        return  helmetCase;
    }
    public void setHelmetCase(int helmetCase){
        this.helmetCase = helmetCase;
    }
} // Last
