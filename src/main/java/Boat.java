public class Boat {
    //Properties
    private String outerType;
    private int flag;

    //Constructors
    public Boat(String inputOuterType, int inputFlag){
        this.outerType = inputOuterType;
        this.flag = inputFlag;
    }

    //Methods
    public int addFlag(){
        return this.addFlag();
    }

    //Getters and Setters
    public String getOuterType() {
        return outerType;
    }
    public void setOuterType(String outerType) {
        this.outerType = outerType;
    }

    public int getFlag() {
        return flag;
    }
    public void setFlag(int flag) {
        this.flag = flag;
    }


} //Last
